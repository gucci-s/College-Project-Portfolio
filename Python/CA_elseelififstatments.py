#grade report using if, elif and else statements
#elif will check certain parameters to choose the right output
#when the input matches a statement it stops and uses that output

grade = 86

if grade >= 90:
  print("A")
elif grade >= 80:
  print("B")
elif grade >= 70:
  print("C")
elif grade >= 60:
  print("D")
else:
  print("F")
